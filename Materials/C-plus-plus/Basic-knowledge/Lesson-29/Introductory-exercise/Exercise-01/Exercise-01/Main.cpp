#include <iostream>

using std::cin;
using std::cout;
using std::endl;

using std::streamsize;
using std::numeric_limits;

void line(int = 10, char = '-');

int main()
{
    cout << "<@> - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - <@>" << endl;

    cout << endl;

    line();
    line(15);
    line(10, '*');
    line(15);
    line();
    //line('*'); // bug

    cout << endl;
    cout << endl;

    cout << "<@> - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - <@>" << endl;

    cin
        .ignore(numeric_limits<streamsize>::max(), '\n');
}

void line(int size, char character)
{
    do
    {
        cout << character;
    } while (size-- != 0);
}
